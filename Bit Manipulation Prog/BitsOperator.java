/*   /// Bitwise Operator///
 *  
 * --- There are 5 types of Bitwise Operator ---
 *   1) Bitwise AND (&)
 *   2) Bitwise OR  (|)
 *   3) Bitwise XOR (^)
 *   4)         NOT (!)
 *   5) SHIFT Operator ---->>   (i) Right Shift (>>)
 *                              (ii) Left Shift(<<)
 * --------------------------------------------------------------------------
 * 
 * __________________ Bitwise AND (&)________________________________________
 *    truth table -
 * 
 *    Val 1 | val 2 = Result
 *     T (1)  &   F (0)  =  F (0)
 *     T (1)  &   T (1)  =  T (1)   ** True(1) **
 *     F (0)  &   T (1)  =  F (0)
 *     F (0)  &   F (0)  =  F (0)
 * 
 *    ##### Gives True if nd only if both values are True --->> 1
 * ________________________________________________________________________
 * ________________________Bitwise OR (|)____________________________________
 *     truth table - 
 * 
 *     Val 1 | val 2 = Result
 *     T (1)  |   F (0)  =  T (1)
 *     T (1)  |   T (1)  =  T (1)   
 *     F (0)  |   T (1)  =  T (1)
 *     F (0)  |   F (0)  =  F (0)  ** False(0) **
 * 
 *    ##### Gives False if nd only if both values are False --->> 0
 * ___________________________________________________________________________
 * ___________________________Bitwise XOR (^)_________________________________
 * 
 *     Val 1 | val 2 = Result
 *     T (1)  |   F (0)  =  T (1)
 *     T (1)  |   T (1)  =  T (0)      1 ^ 1 = 0
 *     F (0)  |   T (1)  =  T (1)
 *     F (0)  |   F (0)  =  F (0)      0 ^ 0 = 0
 * ___________________________________________________________________________
 * _________________________Bitwise NOT(!)____________________________________
 * 
 *      0 ----->> 1
 *      1 ----->> 0
 * ___________________________________________________________________________
 * ___________________ Shift Operator ________________________________________
 *  1) Right Shift (>>) 
 *    shifting the value into right side by 1.
 *   1010 (9) >> 1 == 0101 (5)Output
 *   1101 >> 1 == 0110 Output
 *   1010 >> 2 == 0010 Output
 * 
 *  2) Left Shift (<<)
 *    shifting the value into left by 1.
 *    1010 << 1 == 0100 Output
 *    1101 << 1 == 1010 Output
 *    1010 << 2 == 1000 Output
 * __________________________________________________________________________
 *        
 *   */